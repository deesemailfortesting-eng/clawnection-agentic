export type DemoProfileSeed = {
  archetype: string;
  scenarioTags: string[];
  profile: RomanticProfile;
};

function createProfileFromSeed(seed: DemoProfileSeed): RomanticProfile {
  return {
    ...seed.profile,
    interests: [...seed.profile.interests],
    values: [...seed.profile.values],
    dealbreakers: [...seed.profile.dealbreakers],
    preferenceAgeRange: { ...seed.profile.preferenceAgeRange },
    lifestyleHabits: { ...seed.profile.lifestyleHabits },
  };
}

